package soft.co.books.domain.service.dto;

/**
 * A DTO.
 */
public class ShowDTO {

    private String elem;
    private boolean val;

    public ShowDTO() {
    }

    public String getElem() {
        return elem;
    }

    public void setElem(String elem) {
        this.elem = elem;
    }

    public boolean isVal() {
        return val;
    }

    public void setVal(boolean val) {
        this.val = val;
    }
}
